public class Operadores_Logicos {
    public static void main(String args[]) {
        boolean v = true;
        boolean f = false;

        System.out.println("v && f = "+(v&&f));
        //faslo

        System.out.println("v || f = "+(v||f));
        //verdadero

        System.out.println("! (v && f) = "+!(v&&f));
        //verdadero
    }
}
